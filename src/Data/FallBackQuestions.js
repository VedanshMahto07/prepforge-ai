// Helper: get N questions for a role+difficulty
export function getQuestions(role, difficulty, count = 5) {
    const key = role.toLowerCase().replace(/\s+/g, "");
    const diff = difficulty.toLowerCase();

    // Map display names to keys
    const roleMap = {
        "frontenddeveloper": "frontend",
        "backenddeveloper": "backend",
        "fullstackdeveloper": "fullstack",
        "reactdeveloper": "react",
        "javadeveloper": "java",
        "pythondeveloper": "python",
        "dataanalyst": "dataanalyst",
        "datascientist": "datascientist",
        "machinelearningengineer": "ml",
        "devopsengineer": "devops",
        "cloudengineer": "cloud",
        "cybersecurityanalyst": "cybersecurity",
        "qaengineer": "qa",
        "businessanalyst": "business",
        "productmanager": "pm",
        "softwareengineer": "fullstack",
        "aiengineer": "ml",
        "nodejsdeveloper": "backend",
        "javascriptdeveloper": "frontend",
    };

    const resolvedRole = roleMap[key] || key;
    const pool = fallbackQuestions[resolvedRole]?.[diff] || fallbackQuestions.frontend.medium;
    const shuffled = [...pool].sort(
        () => Math.random() - 0.5
    );

    return shuffled.slice(
        0,
        Math.min(count, shuffled.length)
    );
}